					 <img src="Cebu.jpg" alt="Cebu">
					 <p>Next up on the list is Cebu island, one of the largest and most important provinces in the Philippines. The provincial capital, Cebu city, is the second largest city in the country right after Manila. The whole island is also filled with small towns and villages that provide different opportunities for adventure or luxury. Another major benefit of stopping by in Cebu is the fact it is very easy to reach other, smaller islands nearby from there.<br>
					If you want to be adventurous, another option is to stay at one of the smaller towns. Even though this means you’ll need to drop your standards a bit, the upside is quite clear. Moalboal is the most popular out of these—a relaxed beach town with good nightlife and cozy beach resorts. The main attractions there are Kawasan Falls, canyoning, and snorkeling with the sardines. A great choice for couples visiting the Philippines is Malapascua island with its stunning secluded beaches, high-end resorts, and excellent diving spots.
